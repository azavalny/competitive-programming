class Solution:
    def canFinish(self, numCourses: int, prerequisites: List[List[int]]) -> bool:
        """
        to take course i course j is a prereq [[course_i, course_j]]

        cycle detection with DFS

        0-numCourses-1 courses store course num if you've visited it before

        """
        # global state of courses we've visited
        state = [0]*numCourses #0 -unvisited, 1-visiting, 2-visited

        adj = [[] for course in range(numCourses)]

        for course, prereq in prerequisites:
            adj[course].append(prereq)
        
        def dfs(course):
            if state[course] == 1:
                return False #cycle detected
            elif state[course] == 2:
                return True #processed course
            
            state[course] = 1

            for prereq in adj[course]:
                if not dfs(prereq):
                    return False

            state[course] = 2
            return True

        for course in range(numCourses):
            if state[course] == 0:
                if not dfs(course):
                    return False
        return True